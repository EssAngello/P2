    private void jbt_okIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbt_okIniciarActionPerformed
        // Maricel
        user = jtf_nombreIniciar.getText();
        passwd = jtf_contraseñaIniciar.getText();
        if(jrb_gerente.isSelected()){
            gerente_inicio = app.identificarGerente(user, passwd);
            if(gerente_inicio != null){
                this.setVisible(false);
                g = new GerenteVista(gerente_inicio);
                g.setVisible(true);
                dispose();
            }
            else
                JOptionPane.showMessageDialog(null, "No eres gerente");
        }
        else if(jrb_jugador.isSelected()){
            inicio_jugador = app.identificarJugador(user, passwd);
            if(inicio_jugador != null){
                this.setVisible(false);
                j = new JugadorVista(inicio_jugador);
                j.setVisible(true);
                dispose();
            }
            else 
                JOptionPane.showMessageDialog(null, "No eres jugador");
        }
        else if (jrb_administrador.isSelected()){
            if(admin.identificarse(user, passwd)){
                this.setVisible(false);
                a = new AdministradorVista();
                a.setVisible(true);
                dispose();
            }
            else
                JOptionPane.showMessageDialog(null, "No eres administrador");
        }
        else{
            JOptionPane.showMessageDialog(null, "No has seleccionado el tipo de usuario que eres");
        }
            
    }//GEN-LAST:event_jbt_okIniciarActionPerformed

